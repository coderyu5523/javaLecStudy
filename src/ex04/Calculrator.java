package ex04;

public class Calculrator {
    public double add(int n1, int n2) {
        return n1 + n2;
    }

    public double minus(int n1, int n2) {
        return n1 - n2;
    }

    public double divide(int n1, int n2) {
        return n1 / n2;
    }

    public double
    multi(int n1, int n2) {
        return n1 * n2;
    }
}
